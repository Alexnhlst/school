public class OperazioniPoli {
  // Metodo per operazione tra interi (somma)
  public int operazione(int a, int b) {
    int sommaInteri = a + b;
    return sommaInteri;
  }

  // Riutilizzo dello stesso metodo con un altro tipo
  
  public double operazione(double c, double d) {
    double sommaDouble = c + d;
    return sommaDouble;
  }
}